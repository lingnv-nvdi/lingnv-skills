class 制度设计器:
    """制度设计器：基于三省六部智慧的制度框架生成器"""
    
    def __init__(self):
        self.模板 = {
            "决策层": {
                "名称": "中书省",
                "职责": "定方向、做决策",
                "人数": "3-5人",
                "机制": "集体议事，最终裁决"
            },
            "审议层": {
                "名称": "门下省",
                "职责": "审核方案、提出建议",
                "人数": "2-3人",
                "机制": "审查可行性，批准或驳回"
            },
            "执行层": {
                "名称": "尚书省",
                "职责": "执行决策、落实任务",
                "人数": "若干",
                "机制": "定期汇报，问题反馈"
            },
            "监察层": {
                "名称": "御史台",
                "职责": "监督执行、纠正偏差",
                "人数": "2-3人",
                "机制": "独立检查，报告上级"
            }
        }
    
    def 生成制度框架(self, 场景: str = "通用") -> Dict[str, Any]:
        """生成适合特定场景的制度框架"""
        框架 = {
            "场景": 场景,
            "生成时间": datetime.now().isoformat(),
            "制度结构": self.模板.copy()
        }
        return 框架
    
    def 输出框架(self, 框架: Dict) -> str:
        """输出制度框架为可读文本"""
        lines = [
            "=" * 50,
            f"制度框架设计 - {框架['场景']}",
            "=" * 50,
            f"生成时间: {框架['生成时间']}",
            "",
            "【制度结构】",
        ]
        
        for 层级, 内容 in 框架['制度结构'].items():
            lines.append(f"\n{层级} ({内容['名称']}):")
            lines.append(f"  职责: {内容['职责']}")
            lines.append(f"  人数: {内容['人数']}")
            lines.append(f"  机制: {内容['机制']}")
        
        lines.append("\n" + "=" * 50)
        return "\n".join(lines)


class 决策审核器:
    """决策审核器：模拟三省六部的决策审核流程"""
    
    def __init__(self):
        self.审核要点 = [
            "是否广泛征求了意见？",
            "是否经过了专业审核？",
            "是否评估了执行风险？",
            "是否明确了责任分工？",
            "是否留有调整余地？",
            "是否设置了监督机制？"
        ]
    
    def 审核决策(self, 决策名称: str, 决策内容: str) -> Dict[str, Any]:
        """对决策进行审核"""
        结果 = {
            "决策名称": 决策名称,
            "决策内容": 决策内容,
            "审核时间": datetime.now().isoformat(),
            "审核流程": [
                {
                    "阶段": "谋议",
                    "状态": "待进行",
                    "说明": "广泛征求意见"
                },
                {
                    "阶段": "起草",
                    "状态": "待进行",
                    "说明": "形成具体方案"
                },
                {
                    "阶段": "审议",
                    "状态": "待进行",
                    "说明": "审核可行性与风险"
                },
                {
                    "阶段": "定案",
                    "状态": "待进行",
                    "说明": "最终批准执行"
                }
            ],
            "审核清单": [{"问题": q, "状态": "待回答"} for q in self.审核要点],
            "建议": [],
            "风险": []
        }
        return 结果
    
    def 添加审核结果(self, 审核结果: Dict, 回答: Dict) -> Dict:
        """更新审核结果"""
        for i, item in enumerate(审核结果["审核清单"]):
            if i < len(回答.get("回答", [])):
                item["状态"] = "已回答"
                item["答案"] = 回答["回答"][i]
        
        # 根据回答生成建议和风险
        未通过 = [item for item in 审核结果["审核清单"] 
                 if item.get("答案", "").lower() in ["否", "未", "无", "缺"]]
        
        if 未通过:
            审核结果["风险"] = [f"审核要点「{item['问题']}」未通过" for item in 未通过]
            审核结果["建议"] = ["建议完善上述未通过的审核要点后重新提交审议"]
        
        # 更新流程状态
        for step in 审核结果["审核流程"]:
            if not 审核结果["风险"]:
                step["状态"] = "通过"
            else:
                step["状态"] = "驳回修改"
        
        return 审核结果


class 权力分析器:
    """权力分析器：分析权力结构与制衡关系"""
    
    def __init__(self):
        self.制衡模型 = {
            "决策权": "定方向、做决定",
            "执行权": "落实任务、实施决策",
            "监督权": "检查执行、纠正偏差",
            "审议权": "审核方案、提出建议"
        }
    
    def 分析结构(self, 角色列表: List[Dict]) -> Dict[str, Any]:
        """分析权力结构"""
        分析结果 = {
            "分析时间": datetime.now().isoformat(),
            "角色数量": len(角色列表),
            "权力分布": {},
            "制衡关系": [],
            "建议": []
        }
        
        # 统计权力分布
        for 角色 in 角色列表:
            名称 = 角色.get("名称", "未知")
            权力 = 角色.get("权力", [])
            分析结果["权力分布"][名称] = 权力
        
        # 检查制衡关系
        权力类型 = set()
        for 角色 in 角色列表:
            权力类型.update(角色.get("权力", []))
        
        分析结果["制衡分析"] = {
            "拥有的权力类型": list(权力类型),
            "是否完整": set(self.制衡模型.keys()) == 权力类型,
            "缺失的权力": list(set(self.制衡模型.keys()) - 权力类型)
        }
        
        # 生成建议
        if not 分析结果["制衡分析"]["是否完整"]:
            分析结果["建议"].append("建议补充缺失的权力类型以实现完整制衡")
        
        # 检查是否有单一角色掌握过多权力
        for 角色1 in 角色列表:
            for 角色2 in 角色列表:
                if 角色1 != 角色2:
                    共同权力 = set(角色1.get("权力", [])) & set(角色2.get("权力", []))
                    if len(共同权力) >= 2:
                        分析结果["制衡关系"].append({
                            "类型": "相互制约",
                            "角色1": 角色1["名称"],
                            "角色2": 角色2["名称"],
                            "共同权力": list(共同权力)
                        })
        
        return 分析结果


class 考课模拟器:
    """考课模拟器：模拟科举时代的考核流程"""
    
    def __init__(self):
        self.四善 = [
            "德义有闻",
            "清慎明著", 
            "公平可称",
            "恪勤匪懈"
        ]
        self.考核等级 = ["上上", "上中", "上下", "中上", "中中", "中下", "下上", "下中", "下下"]
    
    def 模拟考核(self, 被考核人: str, 考绩: Dict[str, str]) -> Dict[str, Any]:
        """模拟考核流程"""
        结果 = {
            "被考核人": 被考核人,
            "考核时间": datetime.now().isoformat(),
            "四善考核": {},
            "综合等级": "待定",
            "建议": []
        }
        
        # 四善考核
        得分 = 0
        for 善 in self.四善:
            评价 = 考绩.get(善, "未评价")
            结果["四善考核"][善] = 评价
            if 评价 in ["优", "上", "卓越"]:
                得分 += 1
        
        # 确定等级
        if 得分 == 4:
            结果["综合等级"] = "上上"
        elif 得分 == 3:
            结果["综合等级"] = "上中"
        elif 得分 == 2:
            结果["综合等级"] = "中上"
        else:
            结果["综合等级"] = "中下"
        
        # 生成建议
        if 结果["综合等级"] in ["上上", "上中"]:
            结果["建议"].append("建议晋升")
        elif 结果["综合等级"] in ["中上", "中中"]:
            结果["建议"].append("继续观察，保持现状")
        else:
            结果["建议"].append("建议培训或调整岗位")
        
        return 结果


